    //! RECORD TABEL
    public void recordTable(){
        model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("KODE BARANG");
        model.addColumn("NAMA BARANG");
        model.addColumn("HARGA BARANG");
        this.tblBarang.setModel(model);
        try {
           stat = k.getCon().prepareStatement("SELECT * FROM tbl_barang");
           rs = stat.executeQuery();
           while(rs.next()){
               Object[] data = {
                   rs.getString("id"),
                   rs.getString("kode_barang"),
                   rs.getString("nama_barang"),
                   rs.getString("harga_barang"),
               };
            model.addRow(data);
        }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        this.clearComponent();
    }
    
    //! PROSES REKAM
    public void onHandleRekam(){
           try {
            stat = k.getCon().prepareStatement("INSERT INTO tbl_barang VALUES (?,?,?,?)");
            stat.setInt(1,0);
            stat.setString(2, this.inputKodeBarang.getText());
            stat.setString(3, this.inputNamaBarang.getText());
            stat.setString(4, this.inputHargaBarang.getText());
            stat.executeUpdate();
            this.recordTable();
            JOptionPane.showMessageDialog(null, "DATA BARANG BERHASIL DI TAMBAH!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    //! PROSES HAPUS
    public void onHandleHapus(){
        try{
            stat = k.getCon().prepareStatement("DELETE FROM tbl_barang WHERE id= ?");
            stat.setString(1, this.inputId.getText());
            stat.executeUpdate();
            this.recordTable();
            JOptionPane.showMessageDialog(null, "DATA BARANG BERHASIL DI HAPUS !");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    //! CLICK TABEL
    public void onHandleClick(){
          this.inputId.setText(model.getValueAt(tblBarang.getSelectedRow(), 0).toString());
          this.inputKodeBarang.setText(model.getValueAt(tblBarang.getSelectedRow(), 1).toString());
          this.inputNamaBarang.setText(model.getValueAt(tblBarang.getSelectedRow(), 2).toString());
          this.inputHargaBarang.setText(model.getValueAt(tblBarang.getSelectedRow(), 3).toString());
    }
    
    //! PROSES EDIT
    public void onHandleEdit(){
         try {       
             stat = k.getCon().prepareStatement("UPDATE tbl_barang SET kode_barang=?,"
            + "nama_barang=?, harga_barang=? WHERE id=?");
            stat.setString(1, this.inputKodeBarang.getText());
            stat.setString(2, this.inputNamaBarang.getText());
            stat.setString(3, this.inputHargaBarang.getText());
            stat.setString(4, this.inputId.getText());
            stat.executeUpdate();
            this.recordTable();
            JOptionPane.showMessageDialog(null, "DATA BARANG BERHASIL DI UBAH !");
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

