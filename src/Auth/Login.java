     public void checkLogin(){
        try {
            this.stat = k.getCon().prepareStatement("select * from tbl_users where "
              + "username='"+this.inputUsername.getText()+"' and password='"+this.inputPassword.getText()+"';");
            this.rs = this.stat.executeQuery(); 
            while (rs.next()){
                this.level = rs.getNString("level");
            }
        if(level == ""){
            JOptionPane.showMessageDialog(null, "Login Gagal Silahkan Cek Kembali!");
        }else{
            switch(this.level){
                case  "admin":
                   Admin admin = new Admin();
                   admin.setVisible(true);
                   this.setVisible(false);   
                   break;
                case "petugas":
                   Petugas petugas = new Petugas();
                   petugas.setVisible(true);
                   this.setVisible(false);
                   break;
            } 
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

